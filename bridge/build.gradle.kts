// ---- TeaVM stubs ---------------------------------------------------------------------------
//
// TeaVM's classlib emulates ~30 JDK packages but ships no java.sql.*, java.beans.*, javax.xml.*,
// jakarta.persistence.*, etc. — and we can't compile classes into restricted packages (`java.*`)
// from Kotlin sources. The fix is a small JAR of generated bytecode placed *first* on the TeaVM
// classpath so its empty stub definitions satisfy reachability analysis for jOOQ / Jackson /
// SLF4J etc. that reference these types only at compile time.
//
// None of the stubbed classes are actually called at runtime in the browser dry-run path:
// CapturingSqlExecutor throws before touching JDBC, log calls go through kotlin-logging's
// no-op SLF4J binding, and Rell's JSON type only triggers when a Rell program uses `json`.
//
// Each stub is an empty interface (for types that look like interfaces) or an empty class with
// a public no-arg constructor (for the rest). TeaVM dead-code-eliminates them aggressively.

data class Stub(val name: String, val isInterface: Boolean = true)

// Generated stub class names are rewritten from `<pkg>/X` (or `<pkg>/X$Inner`) into
// `org/teavm/classlib/<pkg>/TX` (or `org/teavm/classlib/<pkg>/TX$Inner`) so teavm-classlib's
// existing T-prefix package renamer (`mapPackageHierarchy|org.teavm.classlib.java=java` +
// `stripPrefixFromPackageHierarchyClasses|org.teavm.classlib.java=T`) translates them back to
// `<pkg>/X` at TeaVM's class-loading layer. This sidesteps the URLClassLoader parent-first
// delegation that would otherwise route every `java.*` lookup through the JDK's bootstrap
// loader and import JDK bytecode (with its full cascade of inner-class / native-method
// references) instead of our empty stubs.
//
// For non-java.* packages (jakarta, javax) we add equivalent mapping rules in
// META-INF/teavm.properties.

fun stubInternalName(name: String): String {
    // Split "<pkg>/<Class>$Inner" into ("<pkg>/", "<Class>$Inner").
    val lastSlash = name.lastIndexOf('/')
    val pkg = name.substring(0, lastSlash)
    val cls = name.substring(lastSlash + 1)
    return "org/teavm/classlib/$pkg/T$cls"
}

