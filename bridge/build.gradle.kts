    // sub-tree we ship stubs for.
    "includePackageHierarchy|javax.xml=true",
)

val generateTeavmStubs by tasks.registering {
    description = "Emits empty stub bytecode for JDK packages TeaVM's classlib omits."
    group = LifecycleBasePlugin.BUILD_GROUP

    inputs.property("stubs", stubs.joinToString { "${it.name}|${it.isInterface}" })
    inputs.property("includes", teavmStubsIncludes.joinToString("\n"))
    outputs.dir(stubsClassesDir)

    doLast {
        // Java 11 class version: V11 = 55. TeaVM is happy with anything <= 21.
        val classVersion = 55
        val outDir = stubsClassesDir.get().asFile
        outDir.deleteRecursively()
        outDir.mkdirs()

        for (stub in stubs) {
            val internalName = stubInternalName(stub.name)
            val cw = org.objectweb.asm.ClassWriter(0)
            val access = if (stub.isInterface) {
                org.objectweb.asm.Opcodes.ACC_PUBLIC or
                        org.objectweb.asm.Opcodes.ACC_INTERFACE or
                        org.objectweb.asm.Opcodes.ACC_ABSTRACT
            } else {
                org.objectweb.asm.Opcodes.ACC_PUBLIC or org.objectweb.asm.Opcodes.ACC_SUPER
            }
            cw.visit(classVersion, access, internalName, null, "java/lang/Object", null)
            if (!stub.isInterface) {
                // Public no-arg constructor: super(); return;
                val mv = cw.visitMethod(org.objectweb.asm.Opcodes.ACC_PUBLIC, "<init>", "()V", null, null)
                mv.visitCode()
                mv.visitVarInsn(org.objectweb.asm.Opcodes.ALOAD, 0)
                mv.visitMethodInsn(
                    org.objectweb.asm.Opcodes.INVOKESPECIAL,
                    "java/lang/Object",
                    "<init>",
                    "()V",
                    false,
                )
                mv.visitInsn(org.objectweb.asm.Opcodes.RETURN)
                mv.visitMaxs(1, 1)
                mv.visitEnd()
            }
            cw.visitEnd()
            val classFile = outDir.resolve("$internalName.class")
            classFile.parentFile.mkdirs()
            classFile.writeBytes(cw.toByteArray())
        }

        // teavm.properties: re-includes the java.* / jakarta.* / javax.* sub-packages we ship.
        val propsFile = outDir.resolve("META-INF/teavm.properties")
        propsFile.parentFile.mkdirs()
        propsFile.writeText(teavmStubsIncludes.joinToString("\n", postfix = "\n"))
    }
}

// Pack the stubs into a JAR so we can prepend it to the TeaVM classpath.
val teavmStubsJar by tasks.registering(Jar::class) {
    description = "Packages TeaVM JDK-stub bytecode into a JAR."
    group = LifecycleBasePlugin.BUILD_GROUP
    dependsOn(generateTeavmStubs)
    from(stubsClassesDir)
    archiveBaseName = "teavm-stubs"
    destinationDirectory = layout.buildDirectory.dir("generated/teavm-stubs")
}

dependencies {
    // Stubs jar must come before rell-base so TeaVM picks our (empty) java.sql.Connection etc.
    // before any other classpath entry that declares them transitively.
    implementation(files(teavmStubsJar.flatMap { it.archiveFile }))

    // The playground only uses the compiler + interpreter + REPL surface in `rell-base` and the
    // glue constants in `rell-api-base`. `rell-tools` (CLI), `rell-api-shell` (shell driver),
    // `rell-api-gtx` (GTX/postchain client), and `rell-gtx` would each drag in tens of
    // megabytes of transitive deps (postgres JDBC, gRPC, jackson, hikari, jline, …) that have
    // nothing to do with running a REPL — TeaVM would spend its reachability budget trying to
    // link them.
    //
    // The `exclude(...)` block below cuts dead-weight transitive dependencies that postchain
    // drags in but no playground code path touches. Each was reached only through static-init
    // chains that the dependency analyser kept widening. Excluding them at the Gradle level
    // is cleaner than chasing every leaf method via the TeaVM stub plugin.
    configurations.named("implementation").configure {
        // Cut dead-weight transitive deps that postchain drags in but no playground code path
        // touches. Each was reached only through static-init chains that TeaVM's dependency
        // analyser kept widening. Excluding them here is cleaner than chasing every leaf
        // method via the TeaVM stub plugin.
        exclude(group = "com.sun.xml.bind")                  // jaxb-impl / jaxb-core — XML binding
        exclude(group = "jakarta.xml.bind")                  // jakarta JAXB API
        exclude(group = "jakarta.activation")                // jakarta.activation-api
        exclude(group = "org.eclipse.angus")                 // angus-activation impl
        exclude(group = "com.beanit", module = "jasn1")      // ASN.1 codecs (postchain crypto path we don't reach)
        exclude(group = "org.graalvm.truffle")               // Truffle backend for rell-base (JVM-only)
        exclude(group = "org.graalvm.polyglot")
        exclude(group = "org.graalvm.sdk")
        exclude(group = "net.postchain.rell", module = "runtime-truffle")
        exclude(group = "io.r2dbc")                          // R2DBC reactive JDBC
        exclude(group = "org.reactivestreams")
        exclude(group = "org.postgresql")                    // PostgreSQL JDBC driver — no DB in browser
        exclude(group = "com.google.code.gson")              // gson — postchain uses it, we don't
    }

    implementation(libs.rell.api.base)

    // teavm-jso brings the @JSExport annotation used to expose static Kotlin methods to JS.
    // The annotation has no runtime semantics on JVM; TeaVM consumes it at compile time.
    compileOnly(libs.teavm.jso)

    // teavm-core: needed to compile the TeaVMPlugin / ClassHolderTransformer at
    // com.chromia.rellplayground.teavm.*. Marked `compileOnly` because the plugin classes are
    // loaded by TeaVM's own daemon (which already has teavm-core on its classpath) — shipping
    // teavm-core itself into the compiled output would bloat the JS bundle with classes TeaVM
    // never reaches at runtime.
    compileOnly(libs.teavm.core)

    // TeaVM classpath. teavm(...) adds the entry to the configuration TeaVM compiles from
    // (in addition to runtimeClasspath). jsoApis provides DOM + JSON types — we use a small
    // subset (e.g. nothing yet) but it's required for the JS frontend's helpers.
    teavm(libs.teavm.jso.apis)
}

// Tensor-probe parameters. Override on the CLI with -Prell.teavm.opt=BALANCED, -Prell.teavm.fast=true.
val probeOpt: OptimizationLevel = (project.findProperty("rell.teavm.opt") as String?)
    ?.let { OptimizationLevel.valueOf(it) } ?: OptimizationLevel.NONE
val probeFast: Boolean = (project.findProperty("rell.teavm.fast") as String?)?.toBoolean() ?: false

teavm {
    all {
        // TeaVM compiles a JVM `main(String[])` entry point. Everything else reachable from
        // it (or from @JSExport methods on the same module) is included.
        mainClass = "com.chromia.rellplayground.PlaygroundJsBridge"
        // Pin every @JSExport-bearing class — without an explicit reference from `main`,
        // TeaVM's dead-code elimination would drop them before the JSExport transformer runs.
        fastGlobalAnalysis = probeFast
        preservedClasses.add("com.chromia.rellplayground.PlaygroundJsBridge")
    }
    js {
        // ESM output — the SPA worker imports the generated module directly.
        moduleType = JSModuleType.ES2015
        optimization = probeOpt
        // Run TeaVM in a forked JVM. The classlib + reachability analysis routinely needs many
        // GB of heap on rell-base + its transitive deps; running in-process competes with
        // Gradle's daemon heap. 8 GB is what the slow dependency-propagation pass needs to
        // converge without thrashing GC.
        outOfProcess = true
        processMemory = 8192
        // Obfuscation halves the minified size — critical for Safari, whose WebKit parser
        // silently stalls compiling our ~90 MB unminified bridge module inside a worker.
        // Variable-name shortening is the cheapest win we have.
        obfuscated = true
        // Sourcemap is a ~50 MB sibling we don't need in production. Keep it off; the stack
        // traces TeaVM emits already carry Kotlin file/line via @SourceFile attributes.
        sourceMap = false
        targetFileName = "rell-playground-bridge.js"
    }
    // WasmGC backend probe — exploring whether 0.15.0-dev-N's WasmGC target compiles where
    // the JS backend currently NPEs (konsoletyper/teavm#1189). Run with ./gradlew :bridge:generateWasmGC.
    wasmGC {
        optimization = probeOpt
        outOfProcess = true
        processMemory = 8192
        obfuscated = true
        sourceMap = false
        targetFileName = "rell-playground-bridge.wasm"
    }
}

